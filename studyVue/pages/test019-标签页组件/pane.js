Vue.component("pane",{
	template:'#paneTemplate',
	props:{
		name:{
			type:String
		},
		label:{
			type:String,
			default:''
		}
	},
	data:function(){
		return{
			show:true
		}
	},
	methods:{
		updateNav:function(){
			this.$parent.updateNav();
		}
	},
	watch:{
		label:function(){
			this.updateNav();
		}
	},
	mounted:function(){
		this.updateNav();
	}
})